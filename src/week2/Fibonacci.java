package week2;

import acm.program.*;


public class Fibonacci extends ConsoleProgram {
	private static final int MAX_TERM_VALUE = 10000;
	public void run() {
	int fib1 = 0;
    int fib2 = 1;
    while (fib1 < 10000) {
    	println(fib1);
    	int fib3 = fib1 + fib2 ;
    	fib1 = fib2;
    	fib2 = fib3;
    	
    			
    	
    }
	}
}

	    	  
	      
	     
	     



	    	  


